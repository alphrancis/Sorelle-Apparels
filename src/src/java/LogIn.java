    private void databaseLogin(){
        String userInput = txtCaptcha.getText();
        if (uname.getText().equals("")) {
            JOptionPane.showMessageDialog(new JFrame(), "Username is required!", "Error", JOptionPane.ERROR_MESSAGE);
            
        
        } else if (pass.getPassword().length == 0) {
            JOptionPane.showMessageDialog(new JFrame(), "Password is required!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        else if (userInput.equals("")){
            JOptionPane.showMessageDialog(new JFrame(), "Captcha is required!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        else if (!userInput.equals(captchaCode)){
        JOptionPane.showMessageDialog(this, "Invalid CAPTCHA. Please try again.");
            txtCaptcha.setText("");
            generateCaptchaImage();
        }
        else {
            String username = uname.getText();
            String password = new String(pass.getPassword());

            try {
                String sUrl = "jdbc:mysql://localhost:3306/sofdes";
                String sUser = "root";
                String sPass = "";
                Connection con = DriverManager.getConnection(sUrl, sUser, sPass);

                String query = "SELECT password FROM sofdes_admin WHERE username = ?";
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, username);
                ResultSet rs = pst.executeQuery();

                if (rs.next()) {
                    String hashedPassword = rs.getString("password");

                    if (BCrypt.checkpw(password, hashedPassword)) {
                        JOptionPane.showMessageDialog(this, "Login Successful!");
                            HomePage homePage = new HomePage();
                            homePage.setVisible(true);
                            this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Incorrect Username or Password", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } 

                rs.close();
                pst.close();
                con.close();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    //Generate captcha
    
    private void generateCaptchaImage() {
        captchaCode = generateRandomCaptcha();
        BufferedImage captchaImage = createCaptchaImage(captchaCode);
        lblCaptcha.setIcon(new ImageIcon(captchaImage));
    }
    
    private String generateRandomCaptcha() {
        String chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789";
        StringBuilder captcha = new StringBuilder();
        Random random = new Random();
        for (int i = 0; i < 4; i++) {
            captcha.append(chars.charAt(random.nextInt(chars.length())));
        }
        return captcha.toString();
    }

    private BufferedImage createCaptchaImage(String captchaCode) {
        int width = 400, height = 50;
        BufferedImage image = new BufferedImage(width, height, BufferedImage.TYPE_INT_RGB);
        Graphics2D g = image.createGraphics();


        g.setColor(Color.white);
        g.fillRect(0, 0, width, height);

        
        Random random = new Random();
        for (int i = 0; i < 50; i++) {
            int x = random.nextInt(width);
            int y = random.nextInt(height);
            g.setColor(new Color(random.nextInt(256), random.nextInt(256), random.nextInt(256)));
            g.fillRect(x, y, 2, 2);
        }


        g.setFont(new Font("Arial", Font.BOLD, 30));
        for (int i = 0; i < captchaCode.length(); i++) {
            int x = 20 + i * 30;
            int y = 30 + random.nextInt(20);
            g.setColor(new Color(random.nextInt(256), random.nextInt(256), random.nextInt(256)));
            g.drawString(String.valueOf(captchaCode.charAt(i)), x, y);
        }

        for (int i = 0; i < 5; i++) {
            int x1 = random.nextInt(width);
            int y1 = random.nextInt(height);
            int x2 = random.nextInt(width);
            int y2 = random.nextInt(height);
            g.setColor(new Color(random.nextInt(256), random.nextInt(256), random.nextInt(256)));
            g.drawLine(x1, y1, x2, y2);
        }

        g.dispose();
        return image;
    }
    
